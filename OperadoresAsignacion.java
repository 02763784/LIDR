
public class OperadoresAsignacion {
	public static void main(String[] args){
		// Operador =
		int valor = 123;
		System.out.println("Operador =     -> " + valor );
		
		// Operador +=
		valor += 10;
		System.out.println("Operador += 10 -> " + valor );
		
		// Operador -=
		valor -= 15;
		System.out.println("Operador -= 15 -> " + valor );
		
		// Operador *=
		valor *= 2;
		System.out.println("Operador *= 2  -> " + valor );

		// Operador /=
		valor /= 4;
		System.out.println("Operador /= 4  -> " + valor );
		
		// Operador %=
		valor %= 10;
		System.out.println("Operador %= 10 -> " + valor );

		// Operador ++
		valor ++;
		System.out.println("Operador ++    -> " + valor );
		
		// Operador --
		valor --;
		System.out.println("Operador --    -> " + valor );
	}
}

