
public class LiteralIntDemo {
	public static void main(String[] args){
		int valorInt = 10; 		// decimal
		int valorHex = 0x000a;	// hexadecimal
		int valorBin = 0b1010;	// binario
		
		System.out.println("valor Int: " + valorInt );
		System.out.println("valor Hex: " + valorHex );
		System.out.println("valor Bin: " + valorBin );
	}
}

