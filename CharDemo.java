
public class CharDemo {
	public static void main(String[] args){
		char charX = 'X';
		// Valor ASCI y Unicode de Y
		char charYASCII = 89;
		System.out.println("charX: "+ charX );
		System.out.println("charY ASCII: "+ charYASCII );

		char charX1 = 'X';
		System.out.println("charX1: "+ charX1 );
		// Incrementar charX1 (88 + 1)
		charX1++;
		System.out.println("charX1++: "+ charX1 );
	}

}

