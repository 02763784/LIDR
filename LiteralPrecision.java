
public class PrecisionDemo {
	public static void main(String[] args){
		double velocidadMbps = 1024d;
		double velocidadMbpsd = 10.24e2; // notación científica
		float velocidadMbpsf = 1024f;
		
		System.out.println("velocidadMbps: "+ velocidadMbps );
		System.out.println("velocidadMbpsd: "+ velocidadMbpsd );
		System.out.println("velocidadMbpsf: "+ velocidadMbpsf );
	}
}

