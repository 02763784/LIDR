
public class LiteralStringDemo {
	public static void main(String[] args){
		// Variables con Literales String
		String tema = "Literales";
		String dosLineas = "Dos\nLineas";
		String comillas = "\"Comillas\"";
		String diagonalInvertida = "C:\\Documentos";
		String tabulador = "\t Tabulador";
		
		// imprimir resultados
		System.out.println(tema);
		System.out.println(dosLineas);
		System.out.println(comillas);
		System.out.println(diagonalInvertida);
		System.out.println(tabulador);
	}
}

