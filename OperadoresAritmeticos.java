
public class OperadoresAritmeticos {
	public static void main(String[] args){
		int a = 10;
		int b = 15;
		float c = 30.0F;
		double pi = 3.141599D;
		
		// Suma
		int ab = a + b;
		System.out.println(ab);
		
		// Resta
		int ba = a - b;
		System.out.println(ba);
		
		// Multiplicación
		double cpi = c * pi;
		System.out.println(cpi);
		
		// División
		double pic = pi / c;
		System.out.println(pic);
		
		// Módulo
		double mod = c % pi;
		System.out.println(mod);
		
	}
}

